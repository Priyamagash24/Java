package looping;

public class For_loop {
	public static void main(String[] args) {
		System.out.println("hi..");
		
		//for(initialization;condition;iteration)
		
		//iteration--increment(i++)  or decrement(i--)
		
		for(int i=1;i<4;i++) {
			System.out.println("hi");
		}
		System.out.println();
		System.out.println("task completed");
	}
	//1.initialization
	//2.condition--true,false means exit from the loop
	//3.true means enter into the block--print
	//4.iteration......i++=i+1,  i--=i-1
	

}
